// Find Out Length, Uppercase, Lowercase, Substring, split
var s = 'Parvez';
var p = 'Ruhul, Amin, Parvez';

// length
console.log(`Number Of Length Is: ${s.length}`);

// uppercase
console.log(`UpperCase Of Given String Is: ${s.toUpperCase()}`);

// lowercase
console.log(`LowerCase Of Given String Is: ${s.toLowerCase()}`);

// Substring
console.log(`Substring Of Given String Is: ${s.substring(0,2).toUpperCase()}`);

// Split
console.log(`Split Of Given String Is: ${s.split('')}`);
console.log(`Split Of Given String Is: ${p.split(',')}`);
